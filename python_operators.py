#Python Operators
m =  129
n = 17
p = [x for x in range(10)]
print(f"List of whole numbers less than 10: {p}")
print(f"First number: {m}\nSecond number: {n}")
print("\nArithmetic Operators")
ans = m + n
print(f"Addition is: {ans}")
ans = m - n
print(f"Subtraction is: {ans}")
ans = m * n
print(f"Multiplication is: {ans}")
ans = m / n
print(f"Division is: {ans}")
ans = m // n
print(f"Floor Division is: {ans}")
ans = m ** n
print(f"Exponentiation is: {ans}")
ans = m % n
print(f"Modulus is: {ans}")
print("\nComparative Operators")
print(m > n)
print(n < m)
print(m >= n)
print(n <= m)
print(m == n)
print(m != n)
print("\nLogical Operators")
print("And: 1 and 1 = ", end= '') 
print(1 and 1)
print("And: 0 and 1 = ", end= '') 
print(0 and 1)
print("And: 1 and 0 = ", end= '') 
print(1 and 0)
print("And: 0 and 0 = ", end= '') 
print(0 and 0)
print("\n")
print("Or: 1 or 1 = ", end= '') 
print(1 or 1)
print("Or: 0 or 1 = ", end= '') 
print(0 or 1)
print("Or: 1 or 0 = ", end= '') 
print(1 or 0)
print("Or: 0 or 0 = ", end= '') 
print(0 or 0)
print("\nBitwise Operators")
print("Bitwise XOR: 1 ^ 1 = ", end= '') 
print(1 ^ 1)
print("Bitwise XOR: 0 ^ 1 = ", end= '') 
print(0 ^ 1)
print("Bitwise XOR: 1 ^ 0 = ", end= '') 
print(1 ^ 0)
print("Bitwise XOR: 0 ^ 0 = ", end= '') 
print(0 ^ 0)
print("\n")
print("Bitwise And: 1 & 1 = ", end= '') 
print(1 & 1)
print("Bitwise And: 0 & 1 = ", end= '') 
print(0 & 1)
print("Bitwise And: 1 & 0 = ", end= '') 
print(1 & 0)
print("Bitwise And: 0 & 0 = ", end= '') 
print(0 & 0)
print("\n")
print("Bitwise Or: 1 | 1 = ", end= '') 
print(1 | 1)
print("Bitwise Or: 0 | 1 = ", end= '') 
print(0 | 1)
print("Bitwise Or: 1 | 0 = ", end= '') 
print(1 | 0)
print("Bitwise Or: 0 | 0 = ", end= '') 
print(0 | 0)
print("\n")
print("\nMembership Operators")
print("IN: 0 in p = ", end= '') 
print(0 in p)
print("NOT IN: 0 in p = ", end= '') 
print(0 not in p)
print("\n")
print("\nIdentity Operators")
print("IS: Data type of 20 is integer = ", end= '') 
print(type(20) is int)
print("IS NOT: Data type of 20 is not string = ", end= '') 
print(20 is not str)
print("\n")
print("\nAssignment/ Augmented assignment Operators")
ans = 20
print(f"=: Assigning an integer literal to a variable: ans = {ans}")
ans += 1
print(f"+=: Addition assignment: ans+={1} = {ans}")
ans -= 1
print(f"+=: Subtraction assignment: ans-={1} = {ans}")
ans *= 2
print(f"*=: Multiplication assignment: ans*={2} = {ans}")
ans /= 4
print(f"/=: Division assignment: ans/={4} = {ans}")




  
 


